    private void Validar() {
        String usuario = txtUsuario.getText();
        String contrasena = new String(txtContraseña.getPassword());
        boolean flag = MetodoUsuarios.ValidadUsuario(usuario, contrasena);
        
        if (flag) {
            //contenido de Datos Usuario[ID, NOMBRE USUARIO, CONTRASEÑA, NOMBRE, DNI, ID_CARGO, CARGO]
            String[] DatosUsuario = MetodoUsuarios.BuscarDatosUsuario(usuario, contrasena);
            frmPrincipal ventana = new frmPrincipal();
            ventana.setID_USUARIO(Integer.parseInt(DatosUsuario[0]));
            ventana.setTextlblUsuario(DatosUsuario[3]);
            ventana.setID_CARGO(Integer.parseInt(DatosUsuario[5]));
            ventana.setTextlblCargo(DatosUsuario[6]);
            ventana.pack();
            ventana.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "DATOS ERRONEOS");
        }
    }
