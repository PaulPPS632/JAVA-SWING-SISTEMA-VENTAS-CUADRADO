    private void CargarCmb(){
        String[] Marcas = MetodoMarcas.BuscarMarcasString("");
        String[] SubCategoria = MetodoCategorias.TodasSubCategorias("");
        cmbMarca.removeAllItems();
        cmbSubCategoria.removeAllItems();
        cmbMarca.addItem("NINGUNO");
        cmbSubCategoria.addItem("NINGUNO");
        for(String item : Marcas){
            cmbMarca.addItem(item);
        }
        for(String item : SubCategoria){
            cmbSubCategoria.addItem(item);
        }
    }
    public void BuscarCargarTabla(){
        Buscado = txtBuscado.getText();
        if(chkFiltro.isSelected()){
            //el valor 0 sera usado en la db para saber si se selecciono una marca o categoria
            int indexMarca = cmbMarca.getSelectedIndex();
            int indexSubcategoria = cmbSubCategoria.getSelectedIndex();
            if(chkEnstock.isSelected()){
                jTable1.setModel(MetodoProducto.BuscarProductoconFiltroEnStock(Buscado, indexMarca,indexSubcategoria));
            }else{
                jTable1.setModel(MetodoProducto.BuscarProductoconFiltro(Buscado, indexMarca,indexSubcategoria));
            }
        }else{
            if(chkEnstock.isSelected()){
                jTable1.setModel(MetodoProducto.BuscarProductoEnStock(Buscado));
            }else{
                jTable1.setModel(MetodoProducto.BuscarProducto(Buscado));
            }
        }
        //jTable1.getColumnModel().getColumn(0).setPreferredWidth(20);
        //jTable1.getColumnModel().getColumn(1).setPreferredWidth(20);
        //jTable1.getColumnModel().getColumn(2).setPreferredWidth(20);
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(500);
        //jTable1.getColumnModel().getColumn(5).setPreferredWidth(30);
        //jTable1.getColumnModel().getColumn(6).setPreferredWidth(20);
    }
