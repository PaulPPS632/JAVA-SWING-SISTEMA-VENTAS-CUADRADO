    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER && getFrmAIndexar() == 1){
            frmVentas.InsertarProducto(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()), indexInsertar);
            dispose();
        }else if(evt.getKeyCode() == KeyEvent.VK_ENTER && getFrmAIndexar() == 2){
            frmIngreso.InsertarProducto(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()), indexInsertar);
            dispose();
        }else if(evt.getKeyCode() == KeyEvent.VK_ENTER && getFrmAIndexar() == 3){
            int row = jTable1.getSelectedRow();
            String prod = jTable1.getValueAt(row, 3).toString() + " " +jTable1.getValueAt(row, 4).toString();
            LugarDeProducto.InsertarProducto(Integer.parseInt(jTable1.getValueAt(row, 0).toString()), prod);
            dispose();
        }else if(evt.getKeyCode() == KeyEvent.VK_ENTER && getFrmAIndexar() == 4){
            int row = jTable1.getSelectedRow();
            int prod = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            RegistroProductos.setId_producto(prod);
            RegistroProductos.insertarDatos();
            dispose();
        }
    }//GEN-LAST:event_jTable1KeyPressed
    
    

