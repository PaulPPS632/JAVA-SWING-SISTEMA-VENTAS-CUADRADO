    private void Filtrar(){
        String codigo;
        String estado;
        codigo = (cmbTipoDocumento.getSelectedIndex() == 1 ? "F001" : "B001");
        if(cmbTipoDocumento.getSelectedIndex() < 1){
            codigo = "";
        }
        codigo += txtCorrelativo.getText();
        estado = (cmbEstado.getSelectedIndex() < 1 ? "": cmbEstado.getSelectedItem().toString());
        SimpleDateFormat formatoFecha = new SimpleDateFormat("YYY-MM-DD");
        
        if(DateChooserInicio.getDate() == null && DateChooserFinal.getDate() != null){
            jTable1.setModel(queries.QueryReturnTable("call registrocomprafiltrado(?,?,?,?,?)", new Object[]{codigo, txtCliente.getText(), estado, DateChooserInicio.getMinSelectableDate(), DateChooserFinal.getDate()}));
        }else if(DateChooserInicio.getDate() != null && DateChooserFinal.getDate() == null){ 
            jTable1.setModel(queries.QueryReturnTable("call registrocomprafiltrado(?,?,?,?,?)", new Object[]{codigo, txtCliente.getText(), estado, DateChooserInicio.getDate(), DateChooserFinal.getMaxSelectableDate()}));
        }else if(DateChooserInicio.getDate() == null && DateChooserFinal.getDate() == null){
           jTable1.setModel(queries.QueryReturnTable("call registrocomprafiltrado(?,?,?,?,?)", new Object[]{codigo, txtCliente.getText(), estado, DateChooserInicio.getMinSelectableDate(), DateChooserFinal.getMaxSelectableDate()})); 
        }
        else if(DateChooserInicio.getDate() != null && DateChooserInicio.getDate() != null){
            jTable1.setModel(queries.QueryReturnTable("call registrocomprafiltrado(?,?,?,?,?)", new Object[]{codigo, txtCliente.getText(), estado,DateChooserInicio.getDate() , DateChooserFinal.getDate()}));
        }
        
    }
