    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (isTextFieldNotEmpty(txtNroIdentidad)
            && isTextFieldNotEmpty(txtNombreCompleto)
            && isTextFieldNotEmpty(txtDireccion)
            && isTextFieldNotEmpty(txtContacto)
            && isTextFieldNotEmpty(txtEmail)) {
            MetodoProveedores.InsertarCliente(cmbTipoDocumento.getSelectedItem().toString(),
                txtNroIdentidad.getText(), txtNombreCompleto.getText(),
                txtContacto.getText(), txtDireccion.getText(), txtEmail.getText());
            btnbusActionPerformed(evt);
            limpiar();
        }else{
            JOptionPane.showMessageDialog(null, "Faltan Datos");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tblPrincipal.getSelectedRow();

        if (filaSeleccionada != -1) {
            // Si hay una fila seleccionada, muestra un JOptionPane con el índice de la fila
            int id = (int) tblPrincipal.getValueAt(filaSeleccionada, 0);
            String cliente = (String) tblPrincipal.getValueAt(filaSeleccionada, 3);
            int opcion = JOptionPane.showConfirmDialog(null, "¿Desea ELIMINAR este CLIENTE: " + cliente + "?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (opcion == JOptionPane.YES_OPTION) {
                MetodoProveedores.EliminarCliente(id);
                btnbusActionPerformed(evt);
            }
        } else {
            // Si no hay ninguna fila seleccionada, muestra un mensaje indicando eso
            JOptionPane.showMessageDialog(null, "No hay ninguna fila seleccionada.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

