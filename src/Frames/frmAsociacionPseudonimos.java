    private static void hidecolumn(JTable tabla, int column_index){
        TableColumn column = tabla.getColumnModel().getColumn(column_index);
        column.setMinWidth(0);
        column.setMaxWidth(0);
        column.setWidth(0);
        column.setPreferredWidth(0);
    }
    public void BUSCAR_CLIENTE_PROVEEDOR(){
        String buscado = txtBusca_ClienteProveedor.getText();
        if(chkDocumento.isSelected()){
            tblClienteProveedor.setModel(MetodoClientesProveedor.BuscarPorDocumento(buscado));
        }
        if(chkNombre.isSelected()){
            tblClienteProveedor.setModel(MetodoClientesProveedor.BuscarPorNombre(buscado));
        }
        hidecolumn(tblClienteProveedor, 0);
        hidecolumn(tblClienteProveedor, 1);
        hidecolumn(tblClienteProveedor, 4);
        hidecolumn(tblClienteProveedor, 5);
        hidecolumn(tblClienteProveedor, 6);
        tblClienteProveedor.getColumnModel().getColumn(3).setPreferredWidth(150);
    }
    public void BUSCAR_SEUDONIMO(){
        String buscado = txtBusca_Seudonimo.getText();
        tblSeudonimo.setModel(MetodoClientesProveedor.Buscar_Seudonimo(buscado));
        hidecolumn(tblSeudonimo, 0);
        hidecolumn(tblSeudonimo, 2);
    }
    public void MarcarFiltros(int i){
        
        if(i == 1){
            chkDocumento.setSelected(true);
            chkNombre.setSelected(false);
        }else if(i == 2){
            chkDocumento.setSelected(false);
            chkNombre.setSelected(true);
        }
        txtBusca_ClienteProveedor.setText(null);
        txtBusca_ClienteProveedor.requestFocus();
        BUSCAR_CLIENTE_PROVEEDOR();
    }
