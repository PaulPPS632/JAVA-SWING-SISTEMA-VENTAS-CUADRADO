    private void tblClienteProveedorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblClienteProveedorKeyPressed
        // TODO add your handling code here:
        int row = tblClienteProveedor.getSelectedRow();
        if(evt.getKeyCode() == KeyEvent.VK_ENTER && row != -1){
            txtCliente_proveedor_seleccionado.setText(tblClienteProveedor.getValueAt(row, 3).toString());
            setId_cliente_proveedor(Integer.parseInt(tblClienteProveedor.getValueAt(row, 0).toString()));
        }
    }//GEN-LAST:event_tblClienteProveedorKeyPressed

