    private void btnAsociarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsociarActionPerformed
        // TODO add your handling code here:
        if(id_cliente_proveedor == 0 || id_seudonimo == 0){
                JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR LOS DOS CAMPOS PARA ASOCIAR");    
        }else{
            int valor = JOptionPane.showConfirmDialog(
                    null,
                    "ESTA SEGURO DE REALIZAR ESTA ASOCIACION",
                    "Confirmación",
                    JOptionPane.YES_NO_OPTION
                );
            if(valor == JOptionPane.YES_OPTION){

                MetodoClientesProveedor.Asociar_ClienteProveedor_Seudonimo(id_cliente_proveedor, id_seudonimo);
            }
        }
    }//GEN-LAST:event_btnAsociarActionPerformed

