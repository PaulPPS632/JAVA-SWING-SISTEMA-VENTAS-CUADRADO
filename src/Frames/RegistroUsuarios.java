    private void limpiar(){
        txtNombreUsuario.setText(null);
        txtNombreCompleto.setText(null);
        txtDni.setText(null);
        pwdContrasena.setText(null);
    }
    private void txtDniKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDniKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String NroDocumento = txtDni.getText();
            String[] RespuestaAPI = Funcionalidades.ConsumoAPI2("dni", NroDocumento);
            txtNombreCompleto.setText(RespuestaAPI[0] + " " + RespuestaAPI[1] + " " + RespuestaAPI[2]);
        }
    }//GEN-LAST:event_txtDniKeyPressed

    private void txtDniKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDniKeyTyped
        // TODO add your handling code here:
        if (!Character.isDigit(evt.getKeyChar()) || txtDni.getText().length() >= 8) {
            evt.consume();
        }
    }//GEN-LAST:event_txtDniKeyTyped

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        limpiar();
        txtNombreUsuario.requestFocus();
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
        // TODO add your handling code here:
        boolean flag = txtNombreUsuario.getText().equals("") || txtNombreCompleto.getText().equals("");
        if(!flag){
            
            MetodoUsuarios.insertarUsuario(txtNombreUsuario.getText(), new String(pwdContrasena.getPassword()), txtNombreCompleto.getText(), txtDni.getText(), (cmbCargo.getSelectedIndex()+1));
            limpiar();
        }
    }//GEN-LAST:event_btnGuardar1ActionPerformed

