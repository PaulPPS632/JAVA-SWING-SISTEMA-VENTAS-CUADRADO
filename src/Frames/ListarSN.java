    private void RendesSeries(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(String sn : ListaSeries){
            model.addRow(new Object[]{sn});
        }
        jTable1.setModel(model);
        int cant = ListaSeries.size();
        jLabel1.setText("CANT: " + cant);
    }   
    private void agregarserie(){
        if(!ListaSeries.contains(txtDato.getText())){
            ListaSeries.add(txtDato.getText());
            RendesSeries(); 
        }
    }
    public static ArrayList<String> leerArchivoSeleccionado() {
        ArrayList<String> series = new ArrayList<>();

        // Configurar el JFileChooser
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Seleccione un archivo");
        fileChooser.setFileFilter(new FileNameExtensionFilter("Archivos de texto", "txt"));

        // Mostrar el diálogo de selección de archivos
        int seleccion = fileChooser.showOpenDialog(null);

        // Verificar si el usuario seleccionó un archivo
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File archivoSeleccionado = fileChooser.getSelectedFile();

            try (BufferedReader br = new BufferedReader(new FileReader(archivoSeleccionado))) {
                String linea;
                while ((linea = br.readLine()) != null) {
                    // Agregar cada línea al ArrayList
                    series.add(linea.trim()); // trim() elimina espacios en blanco al principio y al final de la línea
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

        return series;
    }
    

