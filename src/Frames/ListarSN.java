    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        if(Afectado == 1){
            frmIngreso.InsertarSeries(index, ListaSeries);
        }else if(Afectado == 2){
            boolean flag = false;
            for(String Serie : ListaSeries){
                flag = queries.querieReturnBoolean("select SeriePerteneceVenta(?,?) as Respuesta", new Object[]{id_venta_Afectada, Serie});
                if(!flag){
                    break;
                }
            }
            if(flag){
                vnt.Devoluciones(ListaSeries);
                RegistrosVentas.RecargarRegistros();
            }else{
                JOptionPane.showMessageDialog(null, "ALGUNA DE LAS SERIES NO PERTENENCE ESTA VENTA");
            }
        }else if(Afectado == 3){
            frmTransferencia.setListaDeSeries(ListaSeries);
        }
        dispose();
    }//GEN-LAST:event_btnIngresarActionPerformed

